import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

import jakarta.persistence.EntityManager;
import java.time.Instant;
import java.time.temporal.ChronoUnit;
import java.util.List;
import java.util.Random;
import java.util.concurrent.atomic.AtomicLong;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.matcris.footyfix.IntegrationTest;
import org.matcris.footyfix.domain.Notification;
import org.matcris.footyfix.domain.enumeration.NotificationStatus;
import org.matcris.footyfix.repository.NotificationRepository;
import org.springframework.beans.factory.annotation.Autowired;
